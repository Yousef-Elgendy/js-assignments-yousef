// Description: Print the multiplication table from 1 to 10 using nested loops.

for (let i = 1; i <= 10; ++i){
    for(let j = 1; j <= 12; ++j){
        console.log(`${i} * ${j} = ${i * j}`);
    }
    console.log("-------------------------------------------");
}